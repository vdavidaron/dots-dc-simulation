"""Render pgfplots .tex snippets from the sweep CSVs into zthesis/figures/.

Each snippet is a self-contained ``figure`` environment using ``pgfplots``
and (for axis 5 and the cross-axis cloud) plain TikZ. Add to the thesis
preamble:

    \\usepackage{pgfplots}
    \\pgfplotsset{compat=1.18}
    \\usetikzlibrary{patterns}
"""
from __future__ import annotations

import csv
import math
from pathlib import Path

HERE = Path(__file__).parent
RESULTS = HERE / "results"
THESIS_DIR = HERE.parent.parent.parent / "zthesis"
FIG_DIR = THESIS_DIR / "figures"


def _read_csv(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        return []
    with path.open(newline="", encoding="utf-8") as fh:
        return list(csv.DictReader(fh))


def _f(d: dict[str, str], key: str, default: float = float("nan")) -> float:
    v = d.get(key, "")
    if v in ("", None, "nan"):
        return default
    try:
        return float(v)
    except ValueError:
        return default


def _is_valid(r: dict[str, str]) -> bool:
    return not math.isnan(_f(r, "Cumulative_Carbon_g"))


def _write(path: Path, body: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(body, encoding="utf-8")


