def _pct(val: float, base: float, decimals: int = 2) -> str:
    if math.isnan(val) or math.isnan(base) or base == 0:
        return r"\textit{n/a}"
    return f"{(val - base) / base * 100:.{decimals}f}\\%"


def _keur(val: float) -> str:
    if math.isnan(val):
        return r"\textit{n/a}"
    return f"{val / 1e3:.0f}"


