# ── Axis 2: BESS sizing — 3 panels (carbon, cost, unserved) ────────────────
def _three_panel(rows, x_key, x_label, x_unit, caption, label):
    if not rows:
        return ""

    def pts(metric_fn):
        return "\n".join(
            f"    ({_f(r, x_key):g},{metric_fn(r):.3f})" for r in rows
        )

    coords_c = pts(lambda r: _f(r, "Cumulative_Carbon_g") / 1e6)
    coords_k = pts(lambda r: _f(r, "Cumulative_Cost_EUR"))
    coords_u = pts(lambda r: _f(r, "Cumulative_Unserved_kWh") / 1000.0)
    # Equal-service baseline (B) is per-row: it serves the same load the EMS
    # served at that point, so it moves with capacity rather than being a single
    # flat line. Plotting it per-row keeps the figure consistent with the
    # per-row percentages in the accompanying table.
    base_coords_c = pts(lambda r: _f(r, "Cumulative_EqualService_Carbon_g") / 1e6)
    base_coords_k = pts(lambda r: _f(r, "Cumulative_EqualService_Cost_EUR"))
    # Reliability reference for unserved energy is the PASSIVE no-battery
    # facility (same PV, same grid limit), not the equal-service baseline (which
    # by construction sheds exactly what the EMS sheds). The gap between this and
    # the EMS curve is the load the battery rescues.
    base_coords_u = pts(lambda r: _f(r, "Cumulative_Passive_Unserved_kWh") / 1000.0)

    body = f"""% Auto-generated by sweep/make_figures.py; do not edit
\\begin{{figure}}[ht]
\\centering
\\begin{{tikzpicture}}
\\begin{{axis}}[
    name=p1,
    width=0.68\\textwidth, height=5.0cm,
    xlabel={{{x_label} {x_unit}}}, ylabel={{$\\widetilde{{C}}_T$ [tCO$_2$]}},
    grid=major, grid style={{gray!15}}, title={{(a) Carbon}}, title style={{font=\\small}},
    legend pos=north west, legend style={{font=\\tiny}},
]
\\addplot[mark=*, blue!70!black, very thick] coordinates {{
{coords_c}
}};
\\addlegendentry{{EMS}}
\\addplot[mark=o, red, dashed, thick] coordinates {{
{base_coords_c}
}};
\\addlegendentry{{equal-service baseline}}
\\end{{axis}}
\\begin{{axis}}[
    name=p2, at={{(p1.south west)}}, anchor=north west, yshift=-16mm,
    width=0.68\\textwidth, height=5.0cm,
    xlabel={{{x_label} {x_unit}}}, ylabel={{$\\widetilde{{K}}_T$ [EUR]}},
    grid=major, grid style={{gray!15}}, title={{(b) Cost}}, title style={{font=\\small}},
    legend pos=north west, legend style={{font=\\tiny}},
]
\\addplot[mark=*, orange!80!black, very thick] coordinates {{
{coords_k}
}};
\\addlegendentry{{EMS}}
\\addplot[mark=o, red, dashed, thick] coordinates {{
{base_coords_k}
}};
\\addlegendentry{{equal-service baseline}}
\\end{{axis}}
\\begin{{axis}}[
    name=p3, at={{(p2.south west)}}, anchor=north west, yshift=-16mm,
    width=0.68\\textwidth, height=5.0cm,
    xlabel={{{x_label} {x_unit}}}, ylabel={{$\\widetilde{{L}}_{{unserved,T}}$ [MWh]}},
    grid=major, grid style={{gray!15}}, title={{(c) Unserved energy}},
    title style={{font=\\small}}, ymin=0,
    legend pos=north east, legend style={{font=\\tiny}},
]
\\addplot[mark=*, red!80!black, very thick] coordinates {{
{coords_u}
}};
\\addlegendentry{{EMS}}
\\addplot[mark=o, gray!60!black, dashed, thick] coordinates {{
{base_coords_u}
}};
\\addlegendentry{{passive (no battery)}}
\\end{{axis}}
\\end{{tikzpicture}}
\\caption{{{caption}}}
\\label{{{label}}}
\\end{{figure}}
"""
    return body


