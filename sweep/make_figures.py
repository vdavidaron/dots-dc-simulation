def fig_axis12() -> None:
    """Carbon-free-operation sweep: realized grid carbon-free share, and the
    carbon/unserved price of raising the CFE floor toward 24/7 green operation."""
    rows = [r for r in _read_csv(RESULTS / "axis12_cfe.csv") if _is_valid(r)]
    if not rows:
        return

    SEASON_STYLE = {
        "winter": ("blue!70!black",  "solid",      "*"),
        "spring": ("green!60!black", "dashed",     "triangle*"),
        "summer": ("red!70!black",   "dotted",     "square*"),
        "autumn": ("orange!70!black","dash dot",   "diamond*"),
    }
    SEASON_ORDER = ["winter", "spring", "summer", "autumn"]
    MONTH_LABEL  = {"winter": "Jan--Feb", "spring": "Apr--May",
                    "summer": "Jul--Aug", "autumn": "Oct--Nov"}

    by_season: dict[str, list[dict]] = {s: [] for s in SEASON_ORDER}
    for r in rows:
        s = r.get("season")
        if not s:
            s = "winter"
        if s in by_season:
            by_season[s].append(r)
            
    for s in SEASON_ORDER:
        by_season[s].sort(key=lambda r: _f(r, "cfe_floor"))

    def _series(season_rows, fn):
        out = []
        for r in season_rows:
            y = fn(r)
            if y == y:  # skip NaN
                out.append(f"    ({_f(r, 'cfe_floor') * 100:g},{y:.3f})")
        return "\n".join(out)

    realized_plots, carbon_plots, unserved_plots = [], [], []

    for s in SEASON_ORDER:
        if not by_season[s]: continue
        color, style, mark = SEASON_STYLE[s]
        ml = MONTH_LABEL[s]
        plot_opts = f"mark={mark}, mark size=2pt, {color}, {style}, very thick"
        
        realized = _series(by_season[s], lambda r: _f(r, "Grid_CFE_Pct"))
        if realized.strip():
            realized_plots.append(f"\\addplot[{plot_opts}] coordinates {{\n{realized}\n}};\n\\addlegendentry{{{ml}}}")

        carbon = _series(by_season[s], lambda r: _f(r, "Cumulative_Carbon_g") / 1e6)
        if carbon.strip():
            carbon_plots.append(f"\\addplot[{plot_opts}] coordinates {{\n{carbon}\n}};\n\\addlegendentry{{{ml}}}")
            
        unserved = _series(by_season[s], lambda r: _f(r, "Cumulative_Unserved_kWh") / 1000.0)
        if unserved.strip():
            unserved_plots.append(f"\\addplot[{plot_opts}] coordinates {{\n{unserved}\n}};\n\\addlegendentry{{{ml}}}")

    body = f"""% Auto-generated by sweep/make_figures.py; do not edit
\\begin{{figure}}[ht]
\\centering
\\begin{{tikzpicture}}
\\begin{{axis}}[
    name=p1,
    width=0.78\\textwidth, height=4.8cm,
    xlabel={{Carbon-free floor [\\%]}}, ylabel={{Realized grid CFE [\\%]}},
    grid=major, grid style={{gray!15}}, title={{(a) Realized grid carbon-free share by season}},
    title style={{font=\\small}}, legend pos=north west, legend style={{font=\\tiny, legend columns=2}},
]
{chr(10).join(realized_plots)}
\\addplot[gray, dashed, thick] coordinates {{(0,0) (100,100)}};
\\addlegendentry{{Floor (y=x)}}
\\end{{axis}}
\\begin{{axis}}[
    name=p2, at={{(p1.south west)}}, anchor=north west, yshift=-16mm,
    width=0.78\\textwidth, height=4.8cm,
    xlabel={{Carbon-free floor [\\%]}}, ylabel={{Carbon [tCO$_2$]}},
    grid=major, grid style={{gray!15}}, title={{(b) Total carbon by season}},
    title style={{font=\\small}}, ymin=0, legend pos=south west, legend style={{font=\\tiny, legend columns=2}},
]
{chr(10).join(carbon_plots)}
\\end{{axis}}
\\begin{{axis}}[
    name=p3, at={{(p2.south west)}}, anchor=north west, yshift=-16mm,
    width=0.78\\textwidth, height=4.8cm,
    xlabel={{Carbon-free floor [\\%]}}, ylabel={{Unserved [MWh]}},
    grid=major, grid style={{gray!15}}, title={{(c) Unserved energy by season}},
    title style={{font=\\small}}, ymin=0, legend pos=north west, legend style={{font=\\tiny, legend columns=2}},
]
{chr(10).join(unserved_plots)}
\\end{{axis}}
\\end{{tikzpicture}}
\\caption{{Seasonal Carbon-free-operation sweep: as the carbon-free floor on grid imports rises, the realized grid carbon-free share tracks it (a). Total carbon falls (b) and unserved energy rises (c) as the battery is increasingly relied on to ride through carbon-intensive grid periods, with severity varying by season.}}
\\label{{fig:res_cfe}}
\\end{{figure}}
"""
    _write(FIG_DIR / "fig_axis12_cfe.tex", body)


