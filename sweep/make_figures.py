# ── Axis 5: MPC threshold sweep — 2 panels (heatmap + replan bars) ─────────
def _static_baseline_sentence(static_unserved: float) -> str:
    if math.isnan(static_unserved):
        return ("The Static-Plan baseline run also hit the broker race and is "
                "not reported.")
    return (f"The Static-Plan baseline (no MPC) accrues "
            f"$\\widetilde{{L}}_{{unserved,T}}\\approx{static_unserved:.0f}$\\,kWh, "
            "in the same range as the swept MPC cells — the MPC's replanning "
            "provides only marginal unserved-load improvement on the realised "
            "forecast-error trace, an empirical contrast against H4.")


def fig_axis5() -> None:
    rows = _read_csv(RESULTS / "axis5_mpc.csv")
    if not rows:
        return
    drifts = [1.0, 5.0, 10.0, 20.0]
    spikes = [0.05, 0.10, 0.20, 0.50]
    cells: dict[tuple[float, float], dict[str, float]] = {}
    static_unserved = float("nan")
    static_replans = float("nan")
    for r in rows:
        if r["label"] == "mpc_static":
            static_unserved = _f(r, "Cumulative_Unserved_kWh")
            static_replans = _f(r, "Replan_Count")
            continue
        d = _f(r, "drift_pct")
        s = _f(r, "spike_frac")
        u = _f(r, "Cumulative_Unserved_kWh")
        rp = _f(r, "Replan_Count")
        if not math.isnan(u):
            cells[(d, s)] = {"unserved": u, "replans": rp}

    if not cells:
        return
    unservs = [c["unserved"] for c in cells.values()]
    u_min, u_max = min(unservs), max(unservs)
    u_range = max(u_max - u_min, 1.0)

    cell_w, cell_h = 1.6, 1.1
    rect_lines, replan_pts = [], []
    for j, d in enumerate(drifts):
        for i, s in enumerate(spikes):
            x0, y0 = i * cell_w, j * cell_h
            x1, y1 = x0 + cell_w, y0 + cell_h
            xm, ym = (x0 + x1) / 2, (y0 + y1) / 2
            if (d, s) in cells:
                c = cells[(d, s)]
                # Stretch colour over the actual range so variation is visible.
                shade = int(15 + 75 * (c["unserved"] - u_min) / u_range)
                rect_lines.append(
                    f"  \\fill[blue!{shade}!white] ({x0:.2f},{y0:.2f}) "
                    f"rectangle ({x1:.2f},{y1:.2f});"
                )
                txt_color = "white" if shade > 50 else "black"
                rect_lines.append(
                    f"  \\node[font=\\tiny, {txt_color}, anchor=center] at "
                    f"({xm:.2f},{ym:.2f}) "
                    f"{{{c['unserved'] / 1000:.1f}\\,MWh\\\\"
                    f"\\scriptsize {c['replans']:.0f} replans}};"
                )
                replan_pts.append((d, s, c["replans"]))
            else:
                rect_lines.append(
                    f"  \\fill[pattern=north east lines, pattern color=gray!50] "
                    f"({x0:.2f},{y0:.2f}) rectangle ({x1:.2f},{y1:.2f});"
                )
                rect_lines.append(
                    f"  \\node[font=\\tiny, gray!60!black, anchor=center] at "
                    f"({xm:.2f},{ym:.2f}) {{\\itshape no data}};"
                )

    xtick_lines = [
        f"  \\node[font=\\scriptsize, anchor=north] at "
        f"({i * cell_w + cell_w / 2:.2f},-0.2) {{{s:g}}};"
        for i, s in enumerate(spikes)
    ]
    ytick_lines = [
        f"  \\node[font=\\scriptsize, anchor=east] at "
        f"(-0.15,{j * cell_h + cell_h / 2:.2f}) {{{d:g}\\%}};"
        for j, d in enumerate(drifts)
    ]
    total_w, total_h = len(spikes) * cell_w, len(drifts) * cell_h

    # Replan-count panel: bar per cell, x = drift index, group by spike.
    bar_pts_per_spike: dict[float, list[str]] = {s: [] for s in spikes}
    for d, s, rp in replan_pts:
        bar_pts_per_spike[s].append(f"({d:g},{rp:.0f})")
    # Add static baseline as a horizontal rule
    static_line = ""
    if not math.isnan(static_replans):
        static_line = (
            f"\\addplot[red, dashed, thick] coordinates {{(0,{static_replans:.0f}) "
            f"(25,{static_replans:.0f})}};\n"
            f"\\node[red, font=\\tiny, anchor=south east] at "
            f"(axis cs:25,{static_replans:.0f}) {{Static plan ({static_replans:.0f})}};\n"
        )
    bar_plots = ""
    colors = ["blue!60", "orange!70", "green!60!black", "purple!70"]
    for color, (s, pts) in zip(colors, bar_pts_per_spike.items()):
        if not pts:
            continue
        bar_plots += (
            f"\\addplot+[mark=*, mark size=2pt, {color}, thick] "
            f"coordinates {{ {' '.join(pts)} }};\n"
            f"\\addlegendentry{{spike={s:g}}}\n"
        )

    body = f"""% Auto-generated by sweep/make_figures.py; do not edit
\\begin{{figure}}[ht]
\\centering
\\begin{{tikzpicture}}[align=center, pattern color=gray!50]
{chr(10).join(rect_lines)}
  \\draw[gray!50] (0,0) rectangle ({total_w:.2f},{total_h:.2f});
{chr(10).join(xtick_lines)}
{chr(10).join(ytick_lines)}
  \\node[font=\\small, anchor=north] at ({total_w / 2:.2f},-0.85)
    {{Demand-spike threshold (fraction)}};
  \\node[font=\\small, rotate=90, anchor=south] at (-0.95,{total_h / 2:.2f})
    {{SOC-drift threshold}};
\\end{{tikzpicture}}
\\\\[6pt]
\\begin{{tikzpicture}}
\\begin{{axis}}[
    width=0.75\\textwidth, height=4.5cm,
    xlabel={{SOC-drift threshold [\\%]}}, ylabel={{Replan count}},
    grid=major, grid style={{gray!15}},
    legend style={{font=\\tiny, at={{(1.02,1)}}, anchor=north west}},
    title={{(b) MPC replan frequency vs. threshold}}, title style={{font=\\small}},
    ymin=0, ymax=1300, xmin=0, xmax=25,
]
{bar_plots}{static_line}
\\end{{axis}}
\\end{{tikzpicture}}
\\caption{{MPC threshold sweep: unserved energy and replan count across SOC-drift and demand-spike threshold combinations.}}
\\label{{fig:res_mpc}}
\\end{{figure}}
"""
    _write(FIG_DIR / "fig_axis5_mpc.tex", body)


